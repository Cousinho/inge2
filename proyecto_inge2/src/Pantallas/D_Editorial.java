    private void FormatoCampos(){
        t_codigo.setDocument(new AlfanumericoEspacio(t_codigo, 10));
        t_nombre.setDocument(new AlfanumericoEspacio(t_nombre, 50));
    }
    
    private void CargarDatos(){
        t_codigo.disable();
        t_codigo.setText(editorial_actual.getCod_Editorial());
        t_nombre.setText(editorial_actual.getNombre_Editorial());
    }
    
    private boolean Guardar(){
        setDatos();
        try {
            return BDEditorial.insertar(editorial_actual);
        } catch (SQLException ex) {
            return false;
        }
    }
    
    private boolean Actualizar(){
        setDatos();
        try {
           return BDEditorial.actualizar(editorial_actual);
           
        } catch (SQLException ex) {
           return false;
        }
    }
    
    private void setDatos(){
        editorial_actual.setCod_Editorial(t_codigo.getText());
        editorial_actual.setNombre(t_nombre.getText());
    }
