    public void actualizartabla(){
        limpiartabla();
        String titulos[] = {"Codigo","Nombre","Autor","Año","Cantidad","Editorial"};
        LLibros.setColumnIdentifiers(titulos);
        try {
            for (Iterator<Libro> it = BDLibro.Lista() .iterator(); it.hasNext();) {
                Libro libro = it.next();
                String Datos[] = {String.valueOf(libro.getCod_libro()),libro.getNombre_libro(),
                libro.getAutor(), libro.getAnho_publicacion(), String.valueOf(libro.getCantidad()), libro.getCod_editorial().getCod_Editorial()};
                LLibros.addRow(Datos);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage());
        }
    }
    
    public void limpiartabla(){
        int tamaño =LLibros.getRowCount()-1;
        for(int i=tamaño; i>=0;i--){
              LLibros.removeRow(i );
        }
    }

